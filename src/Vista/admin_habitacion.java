    private void jbejecutaraccionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbejecutaraccionActionPerformed
        int ax = JOptionPane.showConfirmDialog(null, "Estas seguro de ejecutar la accion?");
        if (ax == JOptionPane.YES_OPTION) {
            switch (modo) {
                case "Alta":
                    if (!comprobarDatos()) {
                        Habitacion habitacion = new Habitacion(hotel, Integer.parseInt(String.valueOf(cbnumcamas.getSelectedItem())), null);
                        oph.altaHabitacion(habitacion);
                        habitaciones = oph.mostrarHabitaciones(hotel.getIdhotel());
                    }

                    cbnumcamas.setSelectedIndex(0);

                    break;
                case "Baja":

                    Habitacion habitacion = (Habitacion) habitaciones.get(indicehabitacion);

                    oph.bajaHabitacion(habitacion);
                    habitaciones = oph.mostrarHabitaciones(hotel.getIdhotel());
                    indicehabitacion = 0;
                    cargarencampos();
                    ponercontadores();
                    botoneraNavegacionActivada(true);

                    break;
                case "Modi":

                    if (!comprobarDatos()) {
                        Habitacion habitacion1 = (Habitacion) habitaciones.get(indicehabitacion);
                        habitacion1.setNumcamas(Integer.parseInt(String.valueOf(cbnumcamas.getSelectedItem())));
                        oph.ModiHabitacion(habitacion1);
                        habitaciones = oph.mostrarHabitaciones(hotel.getIdhotel());

                    }

                    break;
            }
        } else {
            JOptionPane.showMessageDialog(null, "No se han realizado cambios en la base de datos");
        }
    }//GEN-LAST:event_jbejecutaraccionActionPerformed

