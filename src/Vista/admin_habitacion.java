    private void cargarencampos() {
        if (habitaciones.isEmpty()) {
            JOptionPane.showMessageDialog(this, "No hay datos en la base de datos", "ERROR", JOptionPane.ERROR_MESSAGE);
            dispose();
        } else {
            Habitacion habitacion = (Habitacion) habitaciones.get(indicehabitacion);
            jtid.setText(String.valueOf(hotel.getIdhotel()));
            jtnombre.setText(hotel.getNombre());
            jtidhabitacion.setText(habitacion.getIdhabitacion().toString());
            for (int i = 0; i < cbnumcamas.getItemCount(); i++) {
                cbnumcamas.setSelectedIndex(i);
                String[] combosplit = String.valueOf(cbnumcamas.getSelectedItem()).split("-");
                if (combosplit[0].equals(String.valueOf(habitacion.getNumcamas()))) {
                    break;
                }
            }
            //meterencombo(habitacion.getIdhabitacion());
            meterencombo(habitacion);
            if (modo.equals("ModiHuesped")) {
                activarhuespedes();
            }

        }

    }

    private boolean comprobarDatos() {
        boolean comprobacion = false;
        String error = "Hay errores en : \n";
        if (jtnombre.getText().length() == 0) {
            error = error + "error en el campo nombre \n";
            comprobacion = true;
        }
        if (jtid.getText().length() == 0) {
            error = error + "error en el campo id \n";
            comprobacion = true;
        }

        return comprobacion;
    }

