    private void meterencombo(Habitacion habitacion) {

        int maximo = 0;
        DefaultListModel huespedes = oph.mostrarHuespedes(habitacion.getIdhabitacion());
        if (huespedes.isEmpty()) {
            if (huespedes.size() < habitacion.getNumcamas()) {
                if (modo.equals("ModiHuesped")) {
                    jbcancelar.setEnabled(true);
                    jbaltahuespedes.setEnabled(true);
                    jbbajahuesped.setEnabled(false);
                }
                huespedes = oph.mostrarHuespedes();
                ArrayList<String> ven = new ArrayList<String>();
                String anadircb;
                for (int i = 0; i < huespedes.size(); i++) {
                    Huesped h = (Huesped) huespedes.get(i);
                    anadircb = h.getDni() + "-" + h.getNombre() + "," + h.getApellidos();
                    ven.add(anadircb);
                }
                ven.add("");
                cbh1.setModel(new DefaultComboBoxModel(ven.toArray()));
                cbh2.setModel(new DefaultComboBoxModel(ven.toArray()));
                cbh3.setModel(new DefaultComboBoxModel(ven.toArray()));
                cbh4.setModel(new DefaultComboBoxModel(ven.toArray()));
                cbh5.setModel(new DefaultComboBoxModel(ven.toArray()));
                cbh1.setSelectedIndex(cbh1.getItemCount() - 1);
                cbh2.setSelectedIndex(cbh1.getItemCount() - 1);
                cbh3.setSelectedIndex(cbh1.getItemCount() - 1);
                cbh4.setSelectedIndex(cbh1.getItemCount() - 1);
                cbh5.setSelectedIndex(cbh1.getItemCount() - 1);
            }
        } else {
            if (modo.equals("ModiHuesped")) {
                jbcancelar.setEnabled(true);
                jbaltahuespedes.setEnabled(false);
                jbbajahuesped.setEnabled(true);
            }
            ArrayList<String> ven = new ArrayList<String>();
            String anadircb;
            for (int i = 0; i < huespedes.size(); i++) {
                Huesped h = (Huesped) huespedes.get(i);
                anadircb = h.getDni() + "-" + h.getNombre() + "," + h.getApellidos();
                ven.add(anadircb);
            }
            if (huespedes.size() > 0) {
                if (huespedes.get(0) != null) {
                    cbh1.setModel(new DefaultComboBoxModel(ven.toArray()));
                    cbh1.setSelectedIndex(0);
                    maximo = 1;
                    if (maximo < huespedes.size()) {
                        cbh2.setModel(new DefaultComboBoxModel(ven.toArray()));
                        cbh2.setSelectedIndex(1);
                        maximo = 2;
                        if (maximo < huespedes.size()) {
                            maximo = 3;
                            cbh3.setModel(new DefaultComboBoxModel(ven.toArray()));
                            cbh3.setSelectedIndex(2);
                            if (maximo < huespedes.size()) {
                                cbh4.setModel(new DefaultComboBoxModel(ven.toArray()));
                                cbh4.setSelectedIndex(3);
                                maximo = 4;
                                if (maximo < huespedes.size()) {
                                    cbh5.setModel(new DefaultComboBoxModel(ven.toArray()));
                                    cbh5.setSelectedIndex(4);
                                }
                            }
                        }
                    }
                }
            } else {
                cbh1.setModel(new DefaultComboBoxModel());
                cbh2.setModel(new DefaultComboBoxModel());
                cbh3.setModel(new DefaultComboBoxModel());
                cbh4.setModel(new DefaultComboBoxModel());
                cbh5.setModel(new DefaultComboBoxModel());

            }

        }
    }

